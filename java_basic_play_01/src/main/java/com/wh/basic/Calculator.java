package com.wh.basic;

public class Calculator {

    /**
     * 加法
     * @param a
     * @param b
     */
    public void add(int a, int b){
        System.out.println(a + b);
    }

    /**
     * 减法
     * @param a
     * @param b
     */
    public void sub(int a, int b){
        System.out.println(a - b);
    }
}
